#include <iostream>
using namespace std;

int main()
{
    int x = -5;
    cout << "n = 3" << endl;
    cout << "a<<1 : " << (x << 1) << endl;
    cout << "a<<2 : " << (x << 2) << endl;
    cout << "a<<3 : " << (x << 3) << endl;
    cout << "a>>1 : " << (x >> 1) << endl;
    cout << "a>>2 : " << (x >> 2) << endl;
    cout << "a>>3 : " << (x >> 3) << endl;
}