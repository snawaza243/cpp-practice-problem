#include <iostream>
using namespace std;

int main()
{
    int n = 5;
    cout << "n = 5" << endl;
    cout << "a<<1 : " << (n << 1) << endl;
    cout << "a<<2 : " << (n << 2) << endl;
    cout << "a<<3 : " << (n << 3) << endl;
    cout << "a>>1 : " << (n >> 1) << endl;
    cout << "a>>2 : " << (n >> 2) << endl;
    cout << "a>>3 : " << (n >> 3) << endl;
}