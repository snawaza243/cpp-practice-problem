#include <iostream>
using namespace std;

int main()
{
    int a = 4;
    int b = 6;
    cout << "a & b\t: " << (a & b) << endl;
    cout << "a | b\t: " << (a | b) << endl;
    cout << "~ a\t: " << ~a << endl;
    cout << "~ b\t: " << ~b << endl;
    cout << "a ^ b\t: " << (a ^ b) << endl;
    return 0;
}